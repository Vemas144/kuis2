    private void daftarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_daftarButtonActionPerformed
        // Validasi semua field terisi
    if (nikText.getText().trim().isEmpty() ||
        namaText.getText().trim().isEmpty() ||
        alamatText.getText().trim().isEmpty() ||
        no_telpTextField.getText().trim().isEmpty() ||
        tgl_lahirFormattedTextField.getText().trim().isEmpty() ||
        ipkFormattedTextField.getText().trim().isEmpty() ||
        pendidikanComboBox.getSelectedIndex() == -1 ||
        (!tidurCheckBox.isSelected() && !makanCheckBox.isSelected() && !bacaCheckBox.isSelected())) {
        
        JOptionPane.showMessageDialog(this,
            "Semua field harus diisi!",
            "Error",
            JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    // Validasi pendidikan
    String pendidikanTerpilih = (String) pendidikanComboBox.getSelectedItem();
    if (pendidikanTerpilih.equals("SD") || 
        pendidikanTerpilih.equals("SMP") || 
        pendidikanTerpilih.equals("SMA")) {
        JOptionPane.showMessageDialog(this,
            "Pendidikan minimal D1!",
            "Error",
            JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    
    // Jika semua validasi berhasil
    JOptionPane.showMessageDialog(this,
        "Anda berhasil mendaftar",
        "Sukses",
        JOptionPane.INFORMATION_MESSAGE);
        
    // Reset form
    resetForm();
}

