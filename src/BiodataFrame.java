    private void tgl_lahirFormattedTextFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tgl_lahirFormattedTextFieldFocusLost
        // TODO add your handling code here:
        try {
            // Format tanggal lahir yang dimasukkan (misalnya "dd/MM/yyyy")
            String tanggalLahir = tgl_lahirFormattedTextField.getText();
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate tanggalLahirDate = LocalDate.parse(tanggalLahir, formatter);

            // Mendapatkan tanggal hari ini
            LocalDate today = LocalDate.now();

            // Menghitung selisih umur dalam tahun
            long usia = ChronoUnit.YEARS.between(tanggalLahirDate, today);

            if (usia < 18) {
                // Jika usia kurang dari 18 tahun, tampilkan peringatan
                JOptionPane.showMessageDialog(this, "Anda belum cukup umur untuk melamar pekerjaan ini", "Peringatan", JOptionPane.WARNING_MESSAGE);
                // Tidak melanjutkan untuk mengisi form
            } 
        } catch (Exception e) {
            // Tangani jika format tanggal salah
            JOptionPane.showMessageDialog(this, "Format tanggal salah. Harap masukkan tanggal dengan format dd/MM/yyyy", "Kesalahan", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tgl_lahirFormattedTextFieldFocusLost

    private void ipkFormattedTextFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_ipkFormattedTextFieldFocusLost
        // TODO add your handling code here:
         try {
        double ipk = Double.parseDouble(ipkFormattedTextField.getText());
        if (ipk < 3.01) {
            JOptionPane.showMessageDialog(this,
                "IPK minimal 3.01!",
                "Error",
                JOptionPane.WARNING_MESSAGE);
            return;
        }
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this,
            "IPK tidak valid!",
            "Error",
            JOptionPane.ERROR_MESSAGE);
        return;
    }
    }//GEN-LAST:event_ipkFormattedTextFieldFocusLost

